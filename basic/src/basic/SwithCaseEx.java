package basic;

public class SwithCaseEx {
	public static void main(String[] args) {
		//0830
		// 틀

		// switch(정수타입) case문은 중국집에서 메뉴 선택 문제와 같음.
		
		
		switch(1) {
			
		case 1:
			System.out.println("짜장면");
			break;
		
		case 2:
			System.out.println("짬뽕");
			break;
		
		case 3:
			System.out.println("탕수육");
			break;
		
		default:
			System.out.println("1~3번 내에서 선택하세욧");
			break;
		
		}
		
		
		
		
	}
}
